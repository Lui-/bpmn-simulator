package com.googlecode.bpmn_simulator.animation.element.visual;

public class Waypoint {

	private final int x;
	private final int y;

	public Waypoint(final int x, final int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

}
